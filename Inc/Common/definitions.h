/*Total of 8bytes -> 8bytes路1uit64_t/8bytes = 1 uit64_t*/
typedef union __attribute__ ((__packed__)) Temperatures {
    uint64_t raw[1];
    struct __attribute__((__packed__)) {
    	 int8_t tempbatt;
         int8_t temp1;
         int8_t temp2;
         int8_t temp3;
         int8_t temp4;
         int8_t temp5;
         int8_t temp6;
    }fields;
} Temperatures;

